-- genres
CREATE TABLE IF NOT EXISTS genres (
    code  VARCHAR(30)  NOT NULL,
    label VARCHAR(100) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (code)
);

-- songs
CREATE TABLE IF NOT EXISTS songs (
    id           INT GENERATED BY DEFAULT AS IDENTITY,
    title        VARCHAR(200) NOT NULL,
    artist       VARCHAR(150) NOT NULL,
    album        VARCHAR(200) DEFAULT NULL,
    genre        VARCHAR(30)  NOT NULL,
    release_year INT          NOT NULL,
    rating       INT          NOT NULL CHECK (rating BETWEEN 1 AND 5),
    notes        TEXT         DEFAULT NULL,
    created_at   TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at   TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    CONSTRAINT fk_songs_genre FOREIGN KEY (genre) REFERENCES genres (code)
);

-- users
CREATE TABLE IF NOT EXISTS users (
    id            INT GENERATED BY DEFAULT AS IDENTITY,
    username      VARCHAR(50)  NOT NULL,
    password_hash VARCHAR(64)  NOT NULL,
    created_at    TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    CONSTRAINT uk_users_username UNIQUE (username)
);


CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_genres_updated_at ON genres;
CREATE TRIGGER trg_genres_updated_at
BEFORE UPDATE ON genres
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

DROP TRIGGER IF EXISTS trg_songs_updated_at ON songs;
CREATE TRIGGER trg_songs_updated_at
BEFORE UPDATE ON songs
FOR EACH ROW EXECUTE FUNCTION set_updated_at();


